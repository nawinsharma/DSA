package StringsLeetcode;

public class PreetyPriniting {
    public static void main(String[] args) {
        float a = 11.2232f;
        System.out.printf("Formatted number is %.2f", a); // 11.22
        System.out.println();
        System.out.println(Math.PI);// 3.141592653589793
        System.out.printf("Formatted value of pi is %.3f", Math.PI); // 3.142
        System.out.println();
        System.out.printf("Hello my name is %s and I am %d years old", "nawin", 19);
    }
}
